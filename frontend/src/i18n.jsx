/**
 * QuantEdge i18n — 简/繁/英三语
 * 架构：简体中文作 key，英文查 EN 字典，繁体查 TW 字典并以 opencc(s2twp) 兜底
 * 旧值 'zh' 自动迁移为 'zh-CN'
 */
import { createContext, useContext, useState, useCallback, useMemo } from 'react';
import * as OpenCC from 'opencc-js';

// Provider 外的安全兜底：行为同 zh-CN（原文穿透 + 参数插值），
// 避免组件在 LangProvider 外（如组件单测）渲染时 useLang() 取到 undefined 而崩溃。
const fallbackInterpolate = (text, params) => {
  if (!text) return '';
  let r = text;
  if (params) Object.entries(params).forEach(([k, v]) => { r = r.replaceAll(`{${k}}`, String(v)); });
  return r;
};
const LangContext = createContext({ lang: 'zh-CN', setLang: () => {}, t: fallbackInterpolate });
const LANG_KEY = 'quantedge_lang';

export const LANGS = ['zh-CN', 'zh-TW', 'en'];
const normalizeLang = (raw) => {
  if (raw === 'zh' || raw === 'zh-CN') return 'zh-CN';
  if (raw === 'zh-TW' || raw === 'zh-HK') return 'zh-TW';
  if (raw === 'en') return 'en';
  return 'zh-CN';
};

// 给 toLocaleString / Intl 用的 BCP-47 locale
export const localeFor = (lang) => lang === 'en' ? 'en-US' : (lang === 'zh-TW' ? 'zh-TW' : 'zh-CN');
// 是否中文（含简繁）
export const isZh = (lang) => lang !== 'en';

// 文本是否含 CJK 字符（用于英文模式下识别"数据里的中文 fallback"是否需要英文替代）
export const hasCJK = (text) => typeof text === 'string' && /[㐀-䶿一-鿿]/.test(text);

// 英文模式下"中文名 → 英文名"兜底：
//   - name 已是英文 → 直接用
//   - name 是中文 → 查 EN 字典；查不到用 ticker 兜底，避免英文界面里出现中文
export const enFallback = (name, ticker) => {
  if (!name) return ticker || '';
  if (!hasCJK(name)) return name;
  return EN[name] || ticker || name;
};

// 无 hook 的翻译：用于非 React 组件场景（如 displayTicker 等纯函数）；
// 行为与 useLang().t 一致：en 查 EN 字典；zh-TW 走 TW 字典 + opencc 兜底；zh-CN 穿透
export const tStatic = (text, lang) => {
  if (!text) return '';
  if (lang === 'en') return EN[text] || text;
  if (lang === 'zh-TW') return translateTW(text);
  return text;
};

const s2twp = OpenCC.Converter({ from: 'cn', to: 'twp' });
// opencc s2twp 在金融/IT 语境下的几处过度转换修正（保持台湾习惯用语）：
//   1) 「代码」→ opencc「程式碼」，但股票/证券代码场景应保留「代碼」
//   2) 「权限」→ opencc「許可權」，但通知/IT 语境台湾标准用「權限」
//   3) 「复盘」→ opencc「覆盤」，但金融「复盘(review)」的「复」是「重复」义，应为「復盤」
// 注：这些是对 opencc 输出的二次替换，只命中连用词，不影响「覆盖→覆蓋」等正常转换。
const TW_POSTFIX = [
  [/程式碼/g, '代碼'],
  [/許可權/g, '權限'],
  [/覆盤/g, '復盤'],
];
const toTW = (text) => {
  let out = s2twp(text);
  for (const [re, rep] of TW_POSTFIX) out = out.replace(re, rep);
  return out;
};

